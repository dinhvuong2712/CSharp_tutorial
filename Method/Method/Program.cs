using System;

namespace Method
{
    class Program
    {
        //Tạo 1 phương thức
        static void Method()
        {
            Console.WriteLine("Hello World!");
        }
        static void Main(string[] args)
        {
            //Method là 1 phương thức chứa các khối câu lệnh chỉ được thực hiện khi gọi phương thức đó
            //Phương thức có thể chứa tham số trong ngoặc tròn để thực hiện vào các câu lệnh trong phương thức đó
            //Cách để gọi phương thức
            Method();
        }
    }
}
